#include "SheetSprite.hpp"

void SheetSprite::Draw(ShaderProgram& program) {
    glBindTexture(GL_TEXTURE_2D, textureID);
    
    GLfloat texCoords[] {
        u, v+height,
        u+width, v,
        u, v,
        u+width, v,
        u, v+height,
        u+width, v+height
    };
    
    float aspect = width/height;
    float vertices[] = {
        -0.5f * size * aspect, -0.5f * size,
        0.5f * size * aspect, 0.5f * size,
        -0.5f * size * aspect, 0.5f * size,
        0.5f * size * aspect, 0.5f * size,
        -0.5f * size * aspect, -0.5f * size,
        0.5f * size * aspect, -0.5f * size
    };
    
    
}
